void main() {
  int nilaiAwal = 10;
  int nilaiAkhir = 100;
  while (nilaiAkhir <= nilaiAkhir) {
    print(nilaiAwal);
    nilaiAkhir++;

    //do while
    int count = 0;
    do {
      print(count);
      count++;
    } while (count <= 10);
  }

  //Infiniti loop
  int count = 1;
  do {
    print(count);
  } while (count <= 10);
}
